import asyncio
import sys
from typing import List

from mcp.server import Server
from mcp.server.stdio import stdio_server
from mcp.types import Tool, TextContent

from tools.i18n import I18nTool
from tools.manifest import ManifestTool
from tools.platform import PlatformTool
from tools.config import ConfigTool
from tools.quality import QualityTool

server = Server("multi-ai-file-paster-mcp")

TOOLS = [
    I18nTool(),
    ManifestTool(),
    PlatformTool(),
    ConfigTool(),
    QualityTool()
]


@server.list_tools()
async def handle_list_tools() -> List[Tool]:
    """List all available Chrome Extension development tools."""
    return [tool.get_definition() for tool in TOOLS]


@server.call_tool()
async def handle_call_tool(name: str, arguments: dict) -> List[TextContent]:
    """Execute a tool by name with provided arguments."""
    for tool in TOOLS:
        if tool.name == name:
            try:
                result = await tool.execute(arguments)
                return [TextContent(type="text", text=result)]
            except Exception as e:
                error_msg = f"âŒ Tool execution failed: {str(e)}"
                return [TextContent(type="text", text=error_msg)]
    
    return [TextContent(type="text", text=f"âŒ Unknown tool: {name}")]


async def main() -> None:
    """Main entry point for MCP server."""
    async with stdio_server() as (read_stream, write_stream):
        await server.run(
            read_stream,
            write_stream,
            server.create_initialization_options()
        )


if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        sys.exit(0)
    except Exception as e:
        print(f"Fatal error: {e}", file=sys.stderr)
        sys.exit(1)

